import streamlit as st

# Настройка заголовка
st.set_page_config(page_title="Limbic.kz", page_icon="🧬")

st.title("🧬 Limbic.kz: Подготовка к олимпиадам")
st.markdown("### Задача №1. Популяция кабанов Алаколя")

# Условие задачи
st.info("""
В популяции 1000 особей. 510 черных кабанов (доминантный признак). 
Популяция в равновесии Харди-Вайнберга. 
**Найдите частоты аллелей А и а.**
""")

# Пошаговые подсказки (Аккордеоны)
with st.expander("Шаг №1: Анализ фенотипов"):
    st.write("Черные кабаны — это и $AA$, и $Aa$. Их общее число 510.")
    st.write("Чтобы найти частоты, нужно сначала найти рецессивов ($aa$), так как их генотип однозначен.")
    st.latex(r"q^2 = \frac{Total - Black}{Total}")

with st.expander("Шаг №2: Математический расчет"):
    st.write("1. Находим количество серых ($aa$): $1000 - 510 = 490$.")
    st.write("2. Частота $q^2 = 0.49$.")
    st.write("3. Извлекаем корень:")
    st.latex(r"q = \sqrt{0.49} = 0.7")

if st.button("Показать финальный ответ"):
    st.success("Частота аллеля a (q) = 0.7; Частота аллеля A (p) = 0.3")
    st.balloons() # Маленький эффект праздника
